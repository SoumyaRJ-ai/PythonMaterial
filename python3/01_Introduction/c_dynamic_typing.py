#!/usr/bin/python
"""
Purpose: Python is a dynamic Typed Language

progr. language
    1. static typed language
        - first declare the variables, & then use them
            int a, float b
            a = 10
    2. Dynamic typed language
        - create when you need. No declaration needed
            num1 = 123
"""
num1 = 100

type(num1)
print(type(num1))
print('num1 =', num1, type(num1))

# Python is a dynamic-typed language
num1 = 300
print('num1 =', num1, type(num1))

num1 = 0.03
print('num1 =', num1, type(num1))

num1 = 0.03j
print('num1 =', num1, type(num1))

num1 = '0.03'
print('num1 =', num1, type(num1))

num1 = '0.03'
print('num1 =', num1, type(num1))

num1 = 'three'
print('num1 =', num1, type(num1))

num1 = True
print('num1 =', num1, type(num1))

num1 = False
print('num1 =', num1, type(num1))

num1 = None
print('num1 =', num1, type(num1))

# NOTE: Strings need to be declared in quotes
